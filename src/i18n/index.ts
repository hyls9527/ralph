let currentLang: Lang = 'zh';

export function getLang(): Lang {
  return currentLang;
}

export function setLang(l: Lang) {
  currentLang = l;
}

export function t(key: keyof typeof dict['zh'], params?: Record<string, string | number>): string {
  let text: string | Function = dict[currentLang]?.[key] ?? dict.zh[key] ?? key;
  if (typeof text === 'function') {
    return key;
  }
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = (text as string).replace(`{${k}}`, String(v));
    });
  }
  return text as string;
}

export function useI18n() {
  const [lang, setLangState] = useState<Lang>(() => {
    const saved = localStorage.getItem('ralph-lang');
    return (saved as Lang) || 'zh';
  });

  const switchLang = useCallback((l: Lang) => {
    setLangState(l);
    setLang(l);
    localStorage.setItem('ralph-lang', l);
  }, []);

  return { lang, switchLang };
}

