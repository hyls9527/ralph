type DictKey = keyof typeof dict['zh'];

interface I18nContextType {
  lang: Lang;
  t: (key: DictKey, params?: Record<string, string | number>) => string;
  switchLang: (l: Lang) => void;
}

const I18nContext = createContext<I18nContextType | null>(null);

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLang] = useState<Lang>(() => {
    if (typeof window !== 'undefined') {
      const saved = localStorage.getItem('ralph-lang');
      return (saved as Lang) || 'zh';
    }
    return 'zh';
  });

  const t = useCallback((key: DictKey, params?: Record<string, string | number>): string => {
    let text = dict[lang]?.[key] ?? dict.zh[key] ?? String(key);
    if (params) {
      Object.entries(params).forEach(([k, v]) => {
        text = (text as string).replace(`{${k}}`, String(v));
      });
    }
    return text as string;
  }, [lang]);

  const switchLang = useCallback((l: Lang) => {
    setLang(l);
    if (typeof window !== 'undefined') {
      localStorage.setItem('ralph-lang', l);
    }
  }, []);

  useEffect(() => {
    document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en';
  }, [lang]);

  return (
    <I18nContext.Provider value={{ lang, t, switchLang }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error('useI18n must be used within I18nProvider');
  }
  return context;
}

export function t(key: DictKey, params?: Record<string, string | number>): string {
  const lang: Lang = (typeof window !== 'undefined' && localStorage.getItem('ralph-lang') as Lang) || 'zh';
  let text = dict[lang]?.[key] ?? dict.zh[key] ?? String(key);
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = (text as string).replace(`{${k}}`, String(v));
    });
  }
  return text as string;
}

export type { Lang };
